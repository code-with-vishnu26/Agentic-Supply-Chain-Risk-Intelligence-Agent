@router.post("/chat")
async def chat_with_agent(request: ChatRequest, db: AsyncSession = Depends(get_db)):
    """
    AI Chatbot - Orchestrates a full multi-agent reasoning cycle.
    1. Planner breaks down the query.
    2. Data Agent fetches relevant context.
    3. RAG Agent retrieves historical patterns.
    4. Risk Agent predicts impact.
    5. Decision Agent suggests mitigations.
    """
    from ..services.brain.agent import run_agent_workflow
    
    # Pre-process the request message into event-like data for the agent
    # In a real system, the Planner Agent would do this extraction.
    mock_event_data = {
        "type": "Query-based Analysis",
        "location": "Global / Detected from query",
        "description": request.message,
        "severity": "medium"
    }

    # Execute the full Multi-Agent Workflow
    try:
        final_state = await run_agent_workflow(mock_event_data)
        
        plan = final_state.get('planner_output', '')
        if "[GENERAL]" in plan:
            response_text = plan.replace("[GENERAL]", "").strip()
            return {"response": response_text}
            
        plan = plan.replace("[RISK_PLAN]", "").strip()
        
        # Construct a rich response from the agentic state
        response_text = f"**Agentic Analysis for:** \"{request.message}\"\n\n"
        response_text += f"**Reasoning:** {plan}\n\n"
        response_text += f"**RAG Context:** {final_state.get('rag_context')}\n\n"
        response_text += f"**Risk Score:** {final_state.get('risk_score') * 100:.1f}/100 ({final_state.get('risk_level').upper()})\n\n"
        
        if final_state.get("mitigation_strategies"):
            response_text += "\n**Recommended Strategies (Data Form):**\n\n"
            response_text += "| Strategy Title | Description | Est. Cost | Risk Reduction |\n"
            response_text += "|---|---|---|---|\n"
            for s in final_state["mitigation_strategies"]:
                cost = s.get('cost', 'N/A')
                response_text += f"| **{s['title']}** | {s['description']} | {cost} | {s['risk_reduction']}% |\n"
        
        return {"response": response_text}
        
    except Exception as e:
        logger.error(f"Agent Orchestration failed: {e}")
        import traceback
        err_detail = traceback.format_exc()
        
        # Local intelligence fallback
        events_res = await db.execute(select(Event).limit(5))
        suppliers_res = await db.execute(select(Supplier).limit(5))
        routes_res = await db.execute(select(Route).limit(3))
        predictions_res = await db.execute(select(RiskPrediction).limit(3))
        
        reply = _build_local_reply(
            request.message, 
            events_res.scalars().all(), 
            suppliers_res.scalars().all(), 
            routes_res.scalars().all(), 
            predictions_res.scalars().all()
        )
        return {"response": f"{reply}\n\n*(Error Detail: {str(e)})*"}

@router.post("/analyze/{event_id}")
async def analyze_event_manual(event_id: str, db: AsyncSession = Depends(get_db)):
    """Manually push an event through the agent brain pipeline."""
    event = await db.scalar(select(Event).where(Event.id == event_id))
    if not event:
        raise HTTPException(status_code=404, detail="Event not found")
    
    result = await process_event(event, db)
    return {"status": "success", "analysis": result}
